package com.teamindecisive.calcurmath.roundoff;

/*** The Geometric Functions ***/
public class GeometricFunctions 
{
	static double sin(double a)
	{
		return Math.sin(a);
	}
	
	static double cos(double a)
	{
		return Math.cos(a);
	}
	
	static double tan(double a)
	{
		return Math.tan(a);
	}
	
	static double arcSin(double a)
	{
		return Math.asin(a);
	}
	
	static double arcCos(double a)
	{
		return Math.acos(a);
	}
	
	static double arcTan(double a)
	{
		return Math.atan(a);
	}
	
	static double sinh(double a)
	{
		return Math.sinh(a);
	}
	
	static double cosh(double a)
	{
		return Math.cosh(a);
	}
	
	static double tanh(double a)
	{
		return Math.tanh(a);
	}
}
